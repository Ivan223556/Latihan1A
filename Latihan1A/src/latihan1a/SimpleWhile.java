package latihan1a;


public class SimpleWhile {
    
    public static void main (String[] args) {
        
        int i = 0;
        int max = 5;
        
        System.out.println("Counting TO " + max);
        
        while (i < max) {
            i++;
            System.out.println("i is : " + i);
        }
    }
}
